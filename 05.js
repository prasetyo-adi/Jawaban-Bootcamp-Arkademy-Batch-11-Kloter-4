//penjumlahan Fibonacci genap

function penjumlahanFibonacci(a,b){
	return a + b ;
}
var a = parseInt(prompt('masukkan bilangan fibonacci genap (2,8,34) !:'));
var b = parseInt(prompt('masukkan bilangan fibonacci genap (2,8,34) lagi !:'));
var hasil = penjumlahanFibonacci(a,b);

console.log(hasil)

